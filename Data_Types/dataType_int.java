package Data_Types;

public class dataType_int {
    public static void main(String[] args) {
        int a = 7;
        int b = 5;
        int sum  = a + b;
        int mul = a * b;
        int div = a / b;
        int rem  = a % b;
        int sub = a-b;

        System.out.println(a + " + " + b + " = " + sum);
        System.out.println(a + " * " + b + " = " + mul);
        System.out.println(a + " / " + b + " = " + div);
        System.out.println(a + " % " + b + " = " + rem);
        System.out.println(a + " - " + b + " = " + sub);
    }
    
}
